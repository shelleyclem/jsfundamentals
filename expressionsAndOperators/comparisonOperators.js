// Comparison Operators

//Equal to 

console.log('3' == 3); //true

//Strict Equal to
//compares the datatype and value of the two values to check if they are equal]

console.log('3' === 3); //false

//Not equal to
console.log('3'!= 3); //false

//Strict not equal to
console.log('3' !== 3); //true

//Greater than
console.log(3 > 2); //true

//less than
console.log(2 < 3); //true

//greater than or equal to
console.log(3 >= 4); //false

//less than or equal to
console.log(2 <= 3); //true

//'And' operator
2 && 3;

//'Or' operator
2 || 3



