//Addition operator
r += 1; //r equals r plus one --- r = r + 1

//Subtraction Operator
r -= 1; //r equals r minus one --- r = r - 1

//Multiplication Operator
r *= 1; // r equals r times one --- r = r * 1

//Division Operator
r /= 1; // r equals r divided by one --- r = r / 1

//Remainder (Modulus) Operator
r %= 1; // r = r % 1

//Exponential Operator
r ** 1; // r = r ** 1

